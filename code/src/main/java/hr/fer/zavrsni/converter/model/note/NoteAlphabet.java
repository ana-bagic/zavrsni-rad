package hr.fer.zavrsni.converter.model.note;

/**
 * Enumeracija modelira moguće tonove note, odnosno moguće tipke na klavijaturi unutar jedne oktave.
 * 
 * @author Ana Bagić
 *
 */
public enum NoteAlphabet {

	/** His/C */
	C,
	/** Cis/Des */
	CSH,
	/** D */
	D,
	/** Dis/Es */
	DSH,
	/** E/Fes */
	E,
	/** Eis/F */
	F,
	/** Fis/Ges */
	FSH,
	/** G */
	G,
	/** Gis/As */
	GSH,
	/** A */
	A,
	/** Ais/B */
	ASH,
	/** H/Ces */
	H
}
